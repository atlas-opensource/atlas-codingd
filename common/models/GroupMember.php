<?php

namespace common\models;

use Yii;

/**
 * This is the model class for table "group_member".
 *
 * @property int $id
 * @property int $user_id
 * @property int $group_id
 *
 * @property Group $group
 * @property User $user
 */
class GroupMember extends \yii\db\ActiveRecord
{
